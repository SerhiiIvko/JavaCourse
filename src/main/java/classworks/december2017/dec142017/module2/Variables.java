public class Variables {
    public static void main(String[] args) {
        byte one = 2;
        short two = 110;
        int three = 3000;
        long four = 10001000L;
        float five = 5.5f;
        double six = 6.6666;
        char seven = 'a';
        boolean eight = true;
        System.out.println("            Примитивные типы данных");
        System.out.println();
        System.out.println("Целочисленные типы:");
        System.out.println("Variable byte is " + one);
        System.out.println("Variable short is " + two);
        System.out.println("Variable int is " + three);
        System.out.println("Variable long is " + four);
        System.out.println();
        System.out.println("Дробные (с плавающей точкой) типы:");
        System.out.println("Variable float is " + five);
        System.out.println("Variable double is " + six);
        System.out.println();
        System.out.println("Символы:");
        System.out.println("Variable char is " + seven);
        System.out.println();
        System.out.println("Логические типы:");
        System.out.println("Variable boolean is " + eight);
    }
}
